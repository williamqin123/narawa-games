function loadTextures(images) {
	textureLoadingProgress.total = images.length;
	for (var imageURL of images) {
		textureLoader.load(imageURL, function(texture) {

			//Grass texture should repeat across the whole surface.

			texture.repeat.set(100, 100);
			texture.wrapS = THREE.RepeatWrapping;
			texture.wrapT = THREE.RepeatWrapping;

			var url = texture.image.src;
			textures[url.substring(url.search("textures"))] = texture;



			textureLoadingProgress.loaded += 1;
			if (textureLoadingProgress.loaded >= textureLoadingProgress.total) {
				addObjects();
				loop();
			}
		});
	}
}

function addObjects() {

	var terrainGeometry = new THREE.PlaneGeometry(50000, 50000, 50, 50);
	var terrainVertices = terrainGeometry.vertices;
	for (var i = 0; i < terrainVertices.length; i += 1) {
		terrainVertices[i].z = terrainHeightMap[i];
	}

	var terrainSmoother = new THREE.SubdivisionModifier(1);
	terrainSmoother.modify(terrainGeometry);

	terrainGeometry.computeFaceNormals();
	terrainGeometry.computeVertexNormals();

	var terrainMaterial = new THREE.MeshLambertMaterial({
		map: textures["textures/grass-texture.png"],
		castShadow: true,
		receiveShadow: true
	});
	terrainMesh = new THREE.Mesh(terrainGeometry, terrainMaterial);
	terrainMesh.position.x = 0;
	terrainMesh.position.y = 0;
	terrainMesh.position.z = 0;

	scene.add(terrainMesh);


	var rockGeometry = new THREE.PlaneGeometry(50000, 50000, 50, 50);
	var rockVertices = rockGeometry.vertices;
	for (var i = 0; i < rockVertices.length; i += 1) {
		rockVertices[i].z = -terrainHeightMap[i] - 500;
	}

	var rockSmoother = new THREE.SubdivisionModifier(1);
	rockSmoother.modify(rockGeometry);

	rockGeometry.computeFaceNormals();
	rockGeometry.computeVertexNormals();

	var rockMaterial = new THREE.MeshLambertMaterial({
		map: textures["textures/rock-texture.png"],
		castShadow: true,
		receiveShadow: true
	});
	rockMesh = new THREE.Mesh(rockGeometry, rockMaterial);
	rockMesh.position.x = 0;
	rockMesh.position.y = 0;
	rockMesh.position.z = 0;

	scene.add(rockMesh);




	var oceanGeometry = new THREE.PlaneGeometry(100000, 100000);
	var oceanMaterial = new THREE.MeshPhongMaterial({
		map: textures["textures/water-texture.png"],
		side: THREE.DoubleSide,
		opacity: 0.5,
		transparent: true,
		castShadow: true,
		receiveShadow: true
	});
	oceanMesh = new THREE.Mesh(oceanGeometry, oceanMaterial);
	oceanMesh.position.x = 0;
	oceanMesh.position.y = 0;
	oceanMesh.position.z = 0;

	scene.add(oceanMesh);


	camera = new THREE.PerspectiveCamera(90, width / height, 1, 10000);
	camera.rotation.order = "ZXY";
	camera.position.z = 5000;

	scene.add(camera);


	sun = new THREE.DirectionalLight(0xffffff, 2);
	sun.position.set(2, 2, 1);
	sun.castShadow = true;
	
	scene.add(sun);

	ambientLight = new THREE.AmbientLight(0xffffee, 0.5);
	scene.add(ambientLight);

	scene.fog = new THREE.Fog(0xaaddff, 500, 10000);

}

function updateWorld() {
	camera.rotation.z -= (mousePosition.x / halfWidth) * speed.cameraRotate * delta;
	camera.rotation.x -= (mousePosition.y / halfHeight) * speed.cameraRotate * delta;

	camera.translateZ(-speed.cameraMove * delta);

	textures["textures/water-texture.png"].offset.x += speed.water * delta;
}

